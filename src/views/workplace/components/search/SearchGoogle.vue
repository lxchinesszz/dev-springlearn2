<script lang="ts">
  // 抽屉工具，支持8个或者是4个
  import { defineComponent, ref, watch } from 'vue';

  export default defineComponent({
    name: 'SearchGoogle',
    props: {
      placeholder: {
        type: String,
        default: '搜索一切',
      },
    },
    emits: ['doAction', 'change', 'blur'],
    setup(props, ctx) {
      const value = ref('');

      // 页面点击搜索触发
      const search = () => {
        if (value.value !== '') {
          ctx.emit('doAction', value.value);
        }
      };

      const blurAction = () => {
        ctx.emit('blur');
      };
      // 监控数据输入框数据变化,发送change事件
      watch(value, (newValue) => {
        ctx.emit('change', newValue);
      });
      return { value, search, blurAction };
    },
  });
</script>

